export function getLatestStatic(kpiId, countryCode) {
  const countryData = STATIC_DATA[kpiId]?.[countryCode];
  if (!countryData) return null;
  const years = Object.keys(countryData).map(Number).sort((a, b) => b - a);
  if (years.length === 0) return null;
  return { value: countryData[years[0]], year: years[0] };
}

export function getRangeStatic(kpiId, countryCode, fromYear, toYear) {
  const countryData = STATIC_DATA[kpiId]?.[countryCode] || {};
  const result = [];
  for (let y = fromYear; y <= toYear; y++) {
    result.push({ year: y, value: countryData[y] ?? null });
  }
  return result;
}
